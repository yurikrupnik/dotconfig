#[cfg(feature = "kubernetes")]
async fn update_configmap(name: &str, namespace: &str, region: &str) -> Result<()> {
    use k8s_openapi::api::core::v1::ConfigMap;
    use kube::{api::PostParams, Api, Client};

    let config = fetch_all_pricing(region).await?;
    let kcl_content = config.to_kcl();

    let client = Client::try_default().await?;
    let cms: Api<ConfigMap> = Api::namespaced(client, namespace);

    let cm = ConfigMap {
        metadata: k8s_openapi::apimachinery::pkg::apis::meta::v1::ObjectMeta {
            name: Some(name.to_string()),
            namespace: Some(namespace.to_string()),
            ..Default::default()
        },
        data: Some(
            [("pricing_config.k".to_string(), kcl_content)]
                .into_iter()
                .collect(),
        ),
        ..Default::default()
    };

    cms.create(&PostParams::default(), &cm).await?;
    println!("ConfigMap {}/{} created/updated", namespace, name);

    Ok(())
}
