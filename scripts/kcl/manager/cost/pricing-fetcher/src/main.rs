// Main
// ============================================================================

#[tokio::main]
async fn main() -> Result<()> {
    let cli = Cli::parse();

    match cli.command {
        Commands::Fetch {
            format,
            region,
            output,
        } => {
            let config = fetch_all_pricing(&region).await?;

            let content = match format.as_str() {
                "kcl" => config.to_kcl(),
                "json" => serde_json::to_string_pretty(&config)?,
                "yaml" => serde_json::to_string_pretty(&config)?, // TODO: Add yaml support
                _ => anyhow::bail!("Unknown format: {}", format),
            };

            if let Some(path) = output {
                std::fs::write(&path, &content)
                    .with_context(|| format!("Failed to write to {}", path))?;
                println!("Pricing data written to {}", path);
            } else {
                println!("{}", content);
            }
        }
        Commands::FetchResource {
            resource_type,
            region,
            format,
        } => {
            let config = fetch_all_pricing(&region).await?;

            let data: serde_json::Value = match resource_type.as_str() {
                "database" => serde_json::to_value(&config.database)?,
                "registry" => serde_json::to_value(&config.registry)?,
                "storage" => serde_json::to_value(&config.storage)?,
                _ => anyhow::bail!("Unknown resource type: {}", resource_type),
            };

            let content = match format.as_str() {
                "json" => serde_json::to_string_pretty(&data)?,
                _ => serde_json::to_string_pretty(&data)?,
            };

            println!("{}", content);
        }
        #[cfg(feature = "kubernetes")]
        Commands::UpdateConfigMap {
            name,
            namespace,
            region,
        } => {
            update_configmap(&name, &namespace, &region).await?;
        }
    }

    Ok(())
}

async fn fetch_all_pricing(region: &str) -> Result<PricingConfig> {
    let mut config = PricingConfig::new(region);

    // Fetch from each provider
    println!("Fetching pricing data...");

    // GCP
    #[cfg(feature = "gcp")]
    {
        println!("  - Fetching GCP pricing...");
        if let Ok(pricing) = gcp::fetch_pricing(region).await {
            config.registry.insert("gcp".to_string(), pricing.registry);
            config
                .database
                .insert("cloudsql".to_string(), pricing.database);
            config.storage.insert("gcs".to_string(), pricing.storage);
        } else {
            println!("    (using defaults for GCP)");
            add_gcp_defaults(&mut config);
        }
    }
    #[cfg(not(feature = "gcp"))]
    add_gcp_defaults(&mut config);

    // AWS
    #[cfg(feature = "aws")]
    {
        println!("  - Fetching AWS pricing...");
        if let Ok(pricing) = aws::fetch_pricing(region).await {
            config.registry.insert("aws".to_string(), pricing.registry);
            config.database.insert("rds".to_string(), pricing.database);
            config.storage.insert("s3".to_string(), pricing.storage);
        } else {
            println!("    (using defaults for AWS)");
            add_aws_defaults(&mut config);
        }
    }
    #[cfg(not(feature = "aws"))]
    add_aws_defaults(&mut config);

    // Azure
    println!("  - Fetching Azure pricing...");
    if let Ok(pricing) = azure::fetch_pricing(region).await {
        config
            .registry
            .insert("azure".to_string(), pricing.registry);
        config
            .database
            .insert("azure_sql".to_string(), pricing.database);
        config
            .storage
            .insert("azure_blob".to_string(), pricing.storage);
    } else {
        println!("    (using defaults for Azure)");
        add_azure_defaults(&mut config);
    }

    // Self-hosted options (always use estimates)
    add_self_hosted_defaults(&mut config);

    config.metadata.source = "api".to_string();

    Ok(config)
}

