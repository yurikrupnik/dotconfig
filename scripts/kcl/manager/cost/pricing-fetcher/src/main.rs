use anyhow::{Context, Result};
use chrono::Utc;
use clap::{Parser, Subcommand};
use serde::{Deserialize, Serialize};
use std::collections::HashMap;

mod providers;

use providers::{azure, PricingProvider};

#[cfg(feature = "gcp")]
use providers::gcp;

#[cfg(feature = "aws")]
use providers::aws;

#[derive(Parser)]
#[command(name = "pricing-fetcher")]
#[command(about = "Fetches real-time cloud pricing for cost-aware provisioning")]
struct Cli {
    #[command(subcommand)]
    command: Commands,
}

#[derive(Subcommand)]
enum Commands {
    /// Fetch all pricing data
    Fetch {
        /// Output format: kcl, json, yaml
        #[arg(short, long, default_value = "kcl")]
        format: String,

        /// Region to fetch pricing for
        #[arg(short, long, default_value = "us-central1")]
        region: String,

        /// Output file (stdout if not specified)
        #[arg(short, long)]
        output: Option<String>,
    },
    /// Fetch specific resource type pricing
    FetchResource {
        /// Resource type: database, registry, storage
        #[arg(short = 't', long)]
        resource_type: String,

        /// Region
        #[arg(short, long, default_value = "us-central1")]
        region: String,

        /// Output format
        #[arg(short, long, default_value = "json")]
        format: String,
    },
    /// Update Kubernetes ConfigMap with pricing data
    #[cfg(feature = "kubernetes")]
    UpdateConfigMap {
        /// ConfigMap name
        #[arg(short, long, default_value = "pricing-config")]
        name: String,

        /// Namespace
        #[arg(short = 's', long, default_value = "platform-system")]
        namespace: String,

        /// Region
        #[arg(short, long, default_value = "us-central1")]
        region: String,
    },
}

// ============================================================================
// Pricing Data Structures (matching KCL schemas)
// ============================================================================

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PricingMetadata {
    pub source: String,
    pub last_updated: String,
    pub region: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RegistryPricing {
    pub storage_per_gb_month: f64,
    pub egress_per_gb: f64,
    pub pull_per_1000: f64,
    pub vulnerability_scan_per_image: f64,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct DatabasePricing {
    pub cpu_per_hour: f64,
    pub memory_per_gb_hour: f64,
    pub storage_per_gb_month: f64,
    pub iops_per_1000_hour: f64,
    pub backup_per_gb_month: f64,
    pub ha_multiplier: f64,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct StoragePricing {
    pub storage_per_gb_month: f64,
    pub egress_per_gb: f64,
    pub operations_per_10k: f64,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PricingConfig {
    pub metadata: PricingMetadata,
    pub registry: HashMap<String, RegistryPricing>,
    pub database: HashMap<String, DatabasePricing>,
    pub storage: HashMap<String, StoragePricing>,
}

impl PricingConfig {
    pub fn new(region: &str) -> Self {
        Self {
            metadata: PricingMetadata {
                source: "api".to_string(),
                last_updated: Utc::now().format("%Y-%m-%d").to_string(),
                region: region.to_string(),
            },
            registry: HashMap::new(),
            database: HashMap::new(),
            storage: HashMap::new(),
        }
    }

    /// Generate KCL configuration file content
    pub fn to_kcl(&self) -> String {
        let mut output = String::new();

        output.push_str("# Pricing Configuration\n");
        output.push_str("# Auto-generated by pricing-fetcher\n");
        output.push_str(&format!("# Last updated: {}\n\n", self.metadata.last_updated));

        // Schemas
        output.push_str(
            r#"schema PricingSource:
    source: str = "api"
    last_updated: str = ""
    region: str = "us-central1"

schema RegistryPricing:
    storage_per_gb_month: float
    egress_per_gb: float
    pull_per_1000: float = 0.0
    vulnerability_scan_per_image: float = 0.0

schema DatabasePricing:
    cpu_per_hour: float
    memory_per_gb_hour: float
    storage_per_gb_month: float
    iops_per_1000_hour: float = 0.0
    backup_per_gb_month: float = 0.0
    ha_multiplier: float = 2.0

schema StoragePricing:
    storage_per_gb_month: float
    egress_per_gb: float
    operations_per_10k: float = 0.0

schema PricingConfig:
    metadata: PricingSource
    registry: {str: RegistryPricing}
    database: {str: DatabasePricing}
    storage: {str: StoragePricing}

"#,
        );

        // PRICING instance
        output.push_str("PRICING = PricingConfig {\n");
        output.push_str("    metadata = PricingSource {\n");
        output.push_str(&format!("        source = \"{}\"\n", self.metadata.source));
        output.push_str(&format!(
            "        last_updated = \"{}\"\n",
            self.metadata.last_updated
        ));
        output.push_str(&format!("        region = \"{}\"\n", self.metadata.region));
        output.push_str("    }\n\n");

        // Registry pricing
        output.push_str("    registry = {\n");
        for (name, pricing) in &self.registry {
            output.push_str(&format!("        {} = RegistryPricing {{\n", name));
            output.push_str(&format!(
                "            storage_per_gb_month = {:.4}\n",
                pricing.storage_per_gb_month
            ));
            output.push_str(&format!(
                "            egress_per_gb = {:.4}\n",
                pricing.egress_per_gb
            ));
            output.push_str(&format!(
                "            vulnerability_scan_per_image = {:.4}\n",
                pricing.vulnerability_scan_per_image
            ));
            output.push_str("        }\n");
        }
        output.push_str("    }\n\n");

        // Database pricing
        output.push_str("    database = {\n");
        for (name, pricing) in &self.database {
            output.push_str(&format!("        {} = DatabasePricing {{\n", name));
            output.push_str(&format!(
                "            cpu_per_hour = {:.4}\n",
                pricing.cpu_per_hour
            ));
            output.push_str(&format!(
                "            memory_per_gb_hour = {:.4}\n",
                pricing.memory_per_gb_hour
            ));
            output.push_str(&format!(
                "            storage_per_gb_month = {:.4}\n",
                pricing.storage_per_gb_month
            ));
            output.push_str(&format!(
                "            iops_per_1000_hour = {:.4}\n",
                pricing.iops_per_1000_hour
            ));
            output.push_str(&format!(
                "            backup_per_gb_month = {:.4}\n",
                pricing.backup_per_gb_month
            ));
            output.push_str(&format!(
                "            ha_multiplier = {:.1}\n",
                pricing.ha_multiplier
            ));
            output.push_str("        }\n");
        }
        output.push_str("    }\n\n");

        // Storage pricing
        output.push_str("    storage = {\n");
        for (name, pricing) in &self.storage {
            output.push_str(&format!("        {} = StoragePricing {{\n", name));
            output.push_str(&format!(
                "            storage_per_gb_month = {:.4}\n",
                pricing.storage_per_gb_month
            ));
            output.push_str(&format!(
                "            egress_per_gb = {:.4}\n",
                pricing.egress_per_gb
            ));
            output.push_str(&format!(
                "            operations_per_10k = {:.4}\n",
                pricing.operations_per_10k
            ));
            output.push_str("        }\n");
        }
        output.push_str("    }\n");

        output.push_str("}\n");

        output
    }
}

